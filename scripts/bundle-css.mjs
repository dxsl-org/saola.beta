#!/usr/bin/env bun
/**
 * bundle-css.mjs
 *
 * Assembles per-widget CSS files from src/saola/ into five distributable bundles
 * in priv/static/, and generates dev/dev-widgets.css for the Vite dev loop.
 *
 * Bundles (all built by ordered concatenation — NOT @import — to avoid Lightning
 * CSS / postcss-import deduplication ambiguity at consumer build time):
 *   priv/static/saola-base.css        — passthrough + base.css body only
 *   priv/static/saola-components.css  — passthrough + base + all component widgets
 *   priv/static/saola-charts.css      — passthrough + base + all chart widgets
 *   priv/static/saola.css             — passthrough + base + components + charts
 *   priv/static/saola-preflight.css   — passthrough + base + global preflight reset
 *
 * dev/dev-widgets.css — generated aggregate with one @import per widget SOURCE file;
 *   consumed by the Vite dev server (HMR watches the @import tree).
 *
 * Validation gates (fail-loud, exit 1):
 *   (a) Any src/saola/*.css (except base.css) not in the manifest → abort.
 *   (b) Any manifest entry whose file is missing → abort.
 *   (c) .build-css/preflight.css missing → abort with instructions.
 *   (d) Bundles verified for single base body + single @layer declaration + single passthrough.
 *
 * Toolchain: bun. Run: bun scripts/bundle-css.mjs
 */

import { readFileSync, writeFileSync, mkdirSync, existsSync, readdirSync } from 'fs';
import { resolve, dirname, relative, join } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(__dirname, '..');

const SRC_SAOLA     = resolve(REPO_ROOT, 'src/saola');
const PRIV_STATIC   = resolve(REPO_ROOT, 'priv/static');
const BUILD_CSS     = resolve(REPO_ROOT, '.build-css');
const MANIFEST_PATH = resolve(__dirname, 'css-bundle-manifest.json');

const BASE_CSS_PATH      = resolve(SRC_SAOLA, 'base.css');
const PREFLIGHT_CSS_PATH = resolve(BUILD_CSS, 'preflight.css');
const DEV_WIDGETS_PATH   = resolve(REPO_ROOT, 'dev/dev-widgets.css');

// Layer declaration for bundles (saola.charts added here; per-widget saola.components
// files already declare it internally — bundles need the full ordering decl once).
const BUNDLE_LAYER_DECL = '@layer saola, saola.theme, saola.base, saola.components, saola.charts;';

const BUNDLE_BANNER = `/* AUTO-GENERATED — do not edit. Run: bun scripts/bundle-css.mjs */\n`;
const DEV_WIDGETS_BANNER = `/* AUTO-GENERATED — do not edit. Generated by bundle-css.mjs from css-bundle-manifest.json. */\n/* Consumed by dev/main.mjs for the Vite dev loop (HMR watches the @import tree). */\n`;

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

/**
 * Print error and exit 1.
 * @param {string} msg
 */
function fatal(msg) {
  console.error(`\nFATAL: ${msg}`);
  process.exit(1);
}

/**
 * Write file only if content changed (preserves mtime for idempotency).
 * @param {string} filePath
 * @param {string} content
 * @returns {boolean}
 */
function writeIfChanged(filePath, content) {
  if (existsSync(filePath)) {
    const existing = readFileSync(filePath, 'utf8');
    if (existing === content) return false;
  }
  writeFileSync(filePath, content, 'utf8');
  return true;
}

// ---------------------------------------------------------------------------
// Step 1: Load and validate manifest
// ---------------------------------------------------------------------------
console.log('bundle-css: loading manifest…');

if (!existsSync(MANIFEST_PATH)) {
  fatal(`Manifest not found: ${MANIFEST_PATH}. Run from repo root with: bun scripts/bundle-css.mjs`);
}

const manifest = JSON.parse(readFileSync(MANIFEST_PATH, 'utf8'));
const { components: componentFiles, charts: chartFiles } = manifest;

if (!Array.isArray(componentFiles) || componentFiles.length === 0) {
  fatal('Manifest "components" array is missing or empty.');
}
if (!Array.isArray(chartFiles) || chartFiles.length === 0) {
  fatal('Manifest "charts" array is missing or empty.');
}

// Build a flat set of all manifest entries (relative to src/saola/).
const allManifestEntries = new Set([...componentFiles, ...chartFiles]);

// (a) Enumerate all src/saola/**/*.css and verify each (except base.css) is in the manifest.
// We check both root-level files and files in component/ subdirectory.
function gatherCssFiles(dir, relBase) {
  const results = [];
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    if (entry.isDirectory()) {
      results.push(...gatherCssFiles(resolve(dir, entry.name), relBase));
    } else if (entry.name.endsWith('.css')) {
      const absPath = resolve(dir, entry.name);
      const relPath = relative(SRC_SAOLA, absPath).replace(/\\/g, '/');
      results.push(relPath);
    }
  }
  return results;
}

const allSourceCssFiles = gatherCssFiles(SRC_SAOLA, SRC_SAOLA);

for (const relPath of allSourceCssFiles) {
  if (relPath === 'base.css') continue; // base.css handled separately
  if (!allManifestEntries.has(relPath)) {
    fatal(
      `Validation (a): src/saola/${relPath} exists but is NOT in css-bundle-manifest.json.\n` +
      `Add it to the appropriate group (components or charts) before bundling.`
    );
  }
}

// (b) Verify every manifest entry has a corresponding file.
for (const entry of allManifestEntries) {
  const absPath = resolve(SRC_SAOLA, entry);
  if (!existsSync(absPath)) {
    fatal(
      `Validation (b): Manifest entry "src/saola/${entry}" has no corresponding file.\n` +
      `Either create the file or remove it from css-bundle-manifest.json.`
    );
  }
}

console.log(`  Manifest OK: ${componentFiles.length} components, ${chartFiles.length} charts`);

// ---------------------------------------------------------------------------
// Step 2: Parse base.css into passthrough + layer-body sections
// ---------------------------------------------------------------------------
console.log('bundle-css: parsing base.css…');

if (!existsSync(BASE_CSS_PATH)) {
  fatal(`base.css not found at ${BASE_CSS_PATH}. Run: bun scripts/build-css.mjs first.`);
}

const baseCssRaw = readFileSync(BASE_CSS_PATH, 'utf8');

/**
 * Split base.css into its two logical sections:
 *   1. passthrough — @property rules + @layer properties block, top-level
 *   2. body — everything after the first @layer saola, ... declaration (inclusive of that decl)
 *
 * The passthrough lines appear BEFORE the "/* Saola cascade layer declaration *\/" comment.
 * The layer declaration line starts with "@layer saola, saola.theme".
 * The body = everything from the @layer theme { ... } and @layer saola.base { ... } blocks.
 *
 * For bundles: emit passthrough ONCE at top, then our own BUNDLE_LAYER_DECL (which includes
 * saola.charts too), then the base body (theme + base layers content, WITHOUT the layer decl line).
 *
 * @returns {{ passthrough: string, baseBody: string }}
 */
function parseBaseCss() {
  const lines = baseCssRaw.split('\n');
  let layerDeclLineIndex = -1;

  for (let i = 0; i < lines.length; i++) {
    const trimmed = lines[i].trim();
    // The layer declaration looks like: @layer saola, saola.theme, saola.base, saola.components;
    if (trimmed.startsWith('@layer saola,') || trimmed === '@layer saola;') {
      layerDeclLineIndex = i;
      break;
    }
  }

  if (layerDeclLineIndex === -1) {
    fatal('Cannot find @layer saola declaration line in base.css. Was it generated by build-css.mjs?');
  }

  // Find the comment line just before the @layer decl (usually a blank line + comment).
  // We take passthrough = everything from line 0 up to (but not including) the comment
  // that precedes the layer decl. To be safe: take everything before the @layer saola decl,
  // strip trailing blank lines + the "/* Saola cascade layer declaration */" comment.
  let passthroughEndIndex = layerDeclLineIndex; // exclusive upper bound

  // Walk back past blank lines and the cascade comment
  while (passthroughEndIndex > 0) {
    const prev = lines[passthroughEndIndex - 1].trim();
    if (prev === '' || prev.startsWith('/* Saola cascade layer declaration')) {
      passthroughEndIndex--;
    } else {
      break;
    }
  }

  // Strip the @generated sentinel comment from passthrough (first line of base.css)
  // and any following comment line that explains the passthrough section.
  let passthroughLines = lines.slice(0, passthroughEndIndex);
  // Remove the sentinel line and the immediately-following description comment
  while (
    passthroughLines.length > 0 &&
    (passthroughLines[0].startsWith('/* @generated') || passthroughLines[0].startsWith('/* Tailwind v4'))
  ) {
    passthroughLines.shift();
  }
  // Trim leading blank lines
  while (passthroughLines.length > 0 && passthroughLines[0].trim() === '') {
    passthroughLines.shift();
  }

  const passthrough = passthroughLines.join('\n').trimEnd();

  // Base body = lines AFTER the @layer saola decl line.
  // Skip the blank line immediately following the decl.
  let bodyStartIndex = layerDeclLineIndex + 1;
  while (bodyStartIndex < lines.length && lines[bodyStartIndex].trim() === '') {
    bodyStartIndex++;
  }

  const baseBody = lines.slice(bodyStartIndex).join('\n').trimEnd();

  return { passthrough, baseBody };
}

const { passthrough: PASSTHROUGH, baseBody: BASE_BODY } = parseBaseCss();

console.log(
  `  Passthrough: ${PASSTHROUGH.split('\n').length} lines, ` +
  `Base body: ${BASE_BODY.split('\n').length} lines`
);

// ---------------------------------------------------------------------------
// Step 3: Load and strip per-widget CSS files
// ---------------------------------------------------------------------------
console.log('bundle-css: loading widget CSS files…');

/**
 * Strip the per-widget file header for use in concatenated bundles:
 *   - @generated sentinel comment
 *   - @layer saola; declaration line
 *   - @import "./base.css"; or @import "../base.css"; line
 *
 * The remaining content (the @layer saola.components / saola.charts block etc.) is returned.
 * Keyframes and saola:custom regions are preserved.
 *
 * @param {string} filePath - absolute path
 * @param {string} relPath - relative path from src/saola/ (for error messages)
 * @returns {string}
 */
function stripWidgetHeader(filePath, relPath) {
  const raw = readFileSync(filePath, 'utf8');
  const lines = raw.split('\n');
  const out = [];
  let headerDone = false;

  // Lines to strip (exact match or pattern):
  //   1. /* @generated saola-css ... */
  //   2. @layer saola;
  //   3. @import "./base.css"; or @import "../base.css"; (exact, with quotes)
  // We strip these only from the TOP of the file (in any order, until we hit
  // something that isn't one of them or a blank line).

  const STRIP_PATTERNS = [
    /^\/\* @generated saola-css/,
    /^@layer saola;\s*$/,
    /^@import ["'](?:\.\/|\.\.\/)?base\.css["'];\s*$/,
  ];

  for (let i = 0; i < lines.length; i++) {
    if (!headerDone) {
      const trimmed = lines[i].trim();
      if (trimmed === '') continue; // skip blank lines in header area
      if (STRIP_PATTERNS.some(p => p.test(trimmed))) continue; // strip this header line
      // First non-header, non-blank line
      headerDone = true;
      out.push(lines[i]);
    } else {
      out.push(lines[i]);
    }
  }

  return out.join('\n').trimEnd();
}

// Load all component widget bodies
const componentBodies = [];
for (const relPath of componentFiles) {
  const absPath = resolve(SRC_SAOLA, relPath);
  const body = stripWidgetHeader(absPath, relPath);
  componentBodies.push({ relPath, body });
}

// Load all chart widget bodies
const chartBodies = [];
for (const relPath of chartFiles) {
  const absPath = resolve(SRC_SAOLA, relPath);
  const body = stripWidgetHeader(absPath, relPath);
  chartBodies.push({ relPath, body });
}

console.log(`  Stripped ${componentBodies.length} component bodies, ${chartBodies.length} chart bodies`);

// ---------------------------------------------------------------------------
// Step 4: Load .build-css/preflight.css
// ---------------------------------------------------------------------------
if (!existsSync(PREFLIGHT_CSS_PATH)) {
  fatal(
    `.build-css/preflight.css not found.\n` +
    `Run: bun scripts/build-css.mjs\n` +
    `(.build-css/ is gitignored and must be regenerated before bundling.)`
  );
}
const preflightCss = readFileSync(PREFLIGHT_CSS_PATH, 'utf8').trimEnd();

// ---------------------------------------------------------------------------
// Step 5: Assemble and emit the 5 bundles
// ---------------------------------------------------------------------------
console.log('bundle-css: assembling bundles…');
mkdirSync(PRIV_STATIC, { recursive: true });

/**
 * Concatenate bundle sections with a blank-line separator.
 * @param {...string} parts
 * @returns {string}
 */
function concat(...parts) {
  return parts.filter(Boolean).join('\n\n') + '\n';
}

/**
 * Emit a single bundle file.
 * @param {string} filename - output filename in priv/static/
 * @param {string} content
 */
function emitBundle(filename, content) {
  const outPath = resolve(PRIV_STATIC, filename);
  const full = BUNDLE_BANNER + content;
  const written = writeIfChanged(outPath, full);
  const sizeKb = (Buffer.byteLength(full, 'utf8') / 1024).toFixed(1);
  console.log(`  ${filename}: ${sizeKb} KB (${written ? 'written' : 'unchanged'})`);
  return full;
}

// saola-base.css — passthrough + layer decl + base body only
const baseBundleContent = concat(
  PASSTHROUGH,
  BUNDLE_LAYER_DECL,
  BASE_BODY
);
emitBundle('saola-base.css', baseBundleContent);

// saola-components.css — passthrough + layer decl + base + all component widget bodies
const componentsBundleContent = concat(
  PASSTHROUGH,
  BUNDLE_LAYER_DECL,
  BASE_BODY,
  componentBodies.map(({ body }) => body).join('\n\n')
);
emitBundle('saola-components.css', componentsBundleContent);

// saola-charts.css — passthrough + layer decl + base + all chart widget bodies
const chartsBundleContent = concat(
  PASSTHROUGH,
  BUNDLE_LAYER_DECL,
  BASE_BODY,
  chartBodies.map(({ body }) => body).join('\n\n')
);
emitBundle('saola-charts.css', chartsBundleContent);

// saola.css — passthrough + layer decl + base + components + charts
const fullBundleContent = concat(
  PASSTHROUGH,
  BUNDLE_LAYER_DECL,
  BASE_BODY,
  componentBodies.map(({ body }) => body).join('\n\n'),
  chartBodies.map(({ body }) => body).join('\n\n')
);
emitBundle('saola.css', fullBundleContent);

// saola-preflight.css — passthrough + layer decl + base + global preflight
const preflightBundleContent = concat(
  PASSTHROUGH,
  BUNDLE_LAYER_DECL,
  BASE_BODY,
  preflightCss
);
emitBundle('saola-preflight.css', preflightBundleContent);

// ---------------------------------------------------------------------------
// Step 6: Verify bundle integrity (single base body + single passthrough + single layer decl)
// ---------------------------------------------------------------------------
console.log('bundle-css: verifying bundle integrity…');

/**
 * Count occurrences of a substring in a string (non-overlapping).
 * @param {string} haystack
 * @param {string} needle
 * @returns {number}
 */
function countOccurrences(haystack, needle) {
  let count = 0;
  let pos = 0;
  while ((pos = haystack.indexOf(needle, pos)) !== -1) {
    count++;
    pos += needle.length;
  }
  return count;
}

// Distinct passthrough fingerprint: first @property rule name from base.css
const firstPropertyMatch = PASSTHROUGH.match(/@property\s+(--[\w-]+)/);
const firstPropertyName = firstPropertyMatch ? firstPropertyMatch[1] : null;

// Base body fingerprint: use the @layer saola.theme opening line
const BASE_BODY_FINGERPRINT = '@layer saola.theme {';

// Layer decl fingerprint
const LAYER_DECL_FINGERPRINT = '@layer saola, saola.theme';

// Full saola.css is the most stringent test
const fullBundleFull = readFileSync(resolve(PRIV_STATIC, 'saola.css'), 'utf8');

const baseBodyCount = countOccurrences(fullBundleFull, BASE_BODY_FINGERPRINT);
const layerDeclCount = countOccurrences(fullBundleFull, LAYER_DECL_FINGERPRINT);
const propCount = firstPropertyName
  ? countOccurrences(fullBundleFull, `@property ${firstPropertyName}`)
  : null;

let integrityOk = true;

if (baseBodyCount !== 1) {
  console.error(`  INTEGRITY FAIL: saola.css contains ${baseBodyCount}x "${BASE_BODY_FINGERPRINT}" (expected 1)`);
  integrityOk = false;
}
if (layerDeclCount !== 1) {
  console.error(`  INTEGRITY FAIL: saola.css contains ${layerDeclCount}x "${LAYER_DECL_FINGERPRINT}" (expected 1)`);
  integrityOk = false;
}
if (propCount !== null && propCount !== 1) {
  console.error(`  INTEGRITY FAIL: saola.css contains ${propCount}x "@property ${firstPropertyName}" (expected 1)`);
  integrityOk = false;
}

if (!integrityOk) {
  fatal('Bundle integrity check failed. See errors above.');
}

console.log(
  `  saola.css integrity OK: ` +
  `base body ×${baseBodyCount}, ` +
  `layer decl ×${layerDeclCount}` +
  (propCount !== null ? `, @property ${firstPropertyName} ×${propCount}` : '')
);

// ---------------------------------------------------------------------------
// Step 7: Generate dev/dev-widgets.css
// ---------------------------------------------------------------------------
console.log('bundle-css: generating dev/dev-widgets.css…');

// Build @import lines: paths relative to dev/ directory
// Component files are at src/saola/<widget>.css
// Component subdirectory files are at src/saola/component/<widget>.css
// dev/ is at <repo-root>/dev/, so relative path from dev/ to src/saola/ is ../src/saola/
const devImportLines = [...componentFiles, ...chartFiles].map((relPath) => {
  return `@import '../src/saola/${relPath}';`;
});

// Fail-loud: verify each import target exists (already verified via manifest check above,
// but an explicit check here makes the error message point at dev-widgets.css generation).
for (const relPath of [...componentFiles, ...chartFiles]) {
  const absPath = resolve(SRC_SAOLA, relPath);
  if (!existsSync(absPath)) {
    fatal(`dev-widgets.css generation: src/saola/${relPath} is missing. Fix manifest or create the file.`);
  }
}

const devWidgetsContent =
  DEV_WIDGETS_BANNER +
  `/* base.css is imported transitively by each widget file via @import "../base.css" */\n\n` +
  devImportLines.join('\n') +
  '\n';

const devWritten = writeIfChanged(DEV_WIDGETS_PATH, devWidgetsContent);
console.log(`  dev/dev-widgets.css: ${devImportLines.length} imports (${devWritten ? 'written' : 'unchanged'})`);

// ---------------------------------------------------------------------------
// Done
// ---------------------------------------------------------------------------
console.log('\nbundle-css: done.');
console.log(`  Bundles emitted to: priv/static/`);
console.log(`  Dev aggregate: dev/dev-widgets.css`);
